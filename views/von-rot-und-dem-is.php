<h2 class="animated fadeIn">Von Rot &amp; dem I.S.<span>2016</span></h2>

<h3 class="animated fadeIn">Die Aufgabe und meine Herangehensweise</h3>

<p class="animated fadeIn">Eine Assoziationskette aus 30 zusammenhängenden Begriffen. Zu jedem Begriff gibt es einen Artikel. Und die Kette setzt sich aus aufeinander verlinkenden Wikipedia-Artikeln zusammen. Eine interessante und nicht ganz alltägliche Aufgabe. Die Herausforderung dabei war, deutlich zu machen das die Begriffe zusammenhängen. Von „Rot“ bis zum „Islamischen Staat“.</p>

<p class="animated fadeIn">Bei der Umsetzung habe ich den Fokus auf die angenehme Lesbarkeit der Texte gelegt. Außerdem war mir wichtig den Betrachter zu animieren sich weiter durch die Animationskette zu klicken. Natürlich habe ich auch eine klare Navigationsstruktur, eindeutige Hierachien und die intuitive Bedinung der Internetseite nicht vernachlässigt.</p>

<p class="animated fadeIn">In dem Entwurf wird der Kettencharakter durch durch vier Dinge verdeutlicht:</p>

<ol class="animated fadeIn">
        <li>Das Menü in Listenform indem man immer den Überblick hat an welcher Stelle man sich in der Assoziationskette befindet</li>
    <li>Die Pfeile mit Vorschau auf den nächsten Artikel zum schnellen Navigieren durch die Kette</li>
    <li>Der letzte Absatz jedes Artikels der einen Anlauftext darstellt, der neugierig auf den nächsten Artikel macht und den Zusammenhang der beiden Begriffe deutlich macht</li>
    <li>Der Link mit Teaser-Bild zum nächsten Artikel am Ende der Seite</li>
</ol>

<p class="animated fadeIn">Mit diesen Konzepten und Schwerpunkten bin ich dann in die Entwurfsphase gestartet.</p>

