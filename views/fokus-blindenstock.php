<h2 class="animated fadeIn">Fokus<span>2017</span></h2>
<h3 class="animated fadeIn">Der digitale Blindenstock</h3>

<p class="animated fadeIn">Fokus nutzt aktuelle Technologien und Datenaustausch um den Alltag von Blinden um ein vielfaches angenehmer zu gestalten und um Probleme die im öffentlichen Raum entstehen zu eliminieren. Anhand meines Designprozesses, der sich grob in Recherche, Ideengenerierung und Lösungsfindung unterteilt, werde ich im Folgenden zeigen wie ich vorgegangen bin. Das Ergebnis ist ein fertig konzipiertes Produkt. Ich heiße Yannic Gräser und studiere im 3. Semester Digitale Medien an der HAWK Hildesheim.</p>

<h3 class="animated fadeIn">Informationsbeschaffung</h3>

<p class="animated fadeIn">Der Bereich Mobilität für Blinde war für mich Neuland. Aus diesem Grund habe ich mich als erstes eindringlich damit beschäftigt wie sich Blinde durch öffentliche Räume bewegen, welche Hilfsmittel genutzt werden und wie sie funktionieren, wie im öffentlichen Raum zum Beispiel durch Infrastruktur auf die Bedürfnisse Blinder eingegangen wird. Um verschiedene Probleme und Abläufe im Alltag eines Blinden zu verstehen habe ich eine User Journey erstellt um zu analysieren wie momentan der Alltag aussieht. So konnte ich den aktuellen Problemen und Stärken näher kommen und Pain-Points analysieren. Außerdem habe ich mich im Rahmen der Informationsbeschaffung mit möglichen Konkurrenzprodukten beschäftigt. Allerdings konnte ich kein Produkt mit ähnlichen Ansprüchen und Ambitionen identifizieren.</p>



