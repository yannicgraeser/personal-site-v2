<p class="animated fadeIn">Mir wurde relativ klar das ich zu Anfang ein grundlegende Entscheidung treffen musste. Will ich ein völlig neues Produkt entwickeln oder will ich den Blindenstock inkrementell verbessern? Der Faktor Zeit und die potentiell höhere Akzeptanz eines verbesserten Blindenstocks den der Nutzer schon kennt (ihm aber trotzdem einen Mehrwert bietet) im Gegensatz zu einem völlig neuen und erst zu erlernenden Produkt waren für mich die entscheidenen Argumente mich für einen inkrementelle Verbesserung zu entscheiden.</p>

