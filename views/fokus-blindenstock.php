<h3 class="animated fadeIn">Ideengenerierung</h3>

<p class="animated fadeIn">Auf Basis der Probleme und Stärken die ich herausgearbeitet habe, habe ich dann Ideen generiert und nach sinnvollen technologischen Ergänzungen zum bestehenden Blindenstock gesucht. Diese Ideen und Technologien habe ich im Anschluss bewertet und mich für die Lösungen entschieden die meine in der Recherchephase herausgearbeiteten Probleme am besten lösen.</p>


