<h3 class="animated fadeIn">Die Funktionen im Überblick</h3>
<p class="animated fadeIn">Diese Funktionen teilen sich in drei Bereiche auf:</p>

<ol class="animated fadeIn">
<li><h3 class="animated fadeIn">Information</h3>
<ul>
<li>Abfahrtszeiten öffentlicher Verkehrsmittel</li>
<li>Informationen über verschiedene Blindenleitsysteme in der Fremde</li>
<li>Haltestellenauskunft</li>
<li>Leitung durch öffentliche Orte mit Hilfe von Beacons</li>
<li>Informationsbeschaffung durch NFC-Labels</li>
</ul>
</li>

<li><h3 class="animated fadeIn">Aktion</h3>
<ul>
<li>Haltewunsch</li>
<li>Taxi rufen</li>
<li>Ampel betätigen</li>
<li>Anruf annehmen</li>
<li>Spracheingabe aktivieren</li>
</ul>
</li>

<li><h3 class="animated fadeIn">Hilfe</h3>
<ul>
<li>Notruf absenden</li>
<li>Ortung per GPS</li>
<li>Abweichungen von „der Norm“ erkennen</li>
</ul>
</li>
</ol>

<p class="animated fadeIn">Ein zusätzlicher Punkt der nicht nur indirekte Vorteile für Nutzer bietet sondern auch für Unternehmen interessant ist, ist die Datenerfassung durch die verbaute Sensorik. Über Cloud-Server können alle möglichen digitalen Geräte, aber auch die Blindenstöcke selber, auf diese Daten zugreifen.</p>

<p class="animated fadeIn">Nachdem ich die Prototypen soweit ausgearbeitet habe, bin ich erneut die User Journey durchgegangen. Diesmal aber mit Fokus, meinem digitalen Blindenstock.</p>


