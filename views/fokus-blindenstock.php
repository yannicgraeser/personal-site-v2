<h3 class="animated fadeIn">Lösungsfindung</h3>

<p class="animated fadeIn">Das Ergebnis der Ideengenerierung ist dieser Prototyp:</p>

<p class="animated fadeIn">Er verfügt über diverse Sensoren die mit Dingen des Internet of Things, Beacons und anderer Sensorik im öffentlichen Raum kommunizieren. Zwei Buttons, die frei konfigurierbar sind, dienen zur Steuerung der verschiedenen Funktionen.</p>


